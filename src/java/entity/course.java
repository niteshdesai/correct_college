
package entity;


public class course {
    private  int c_id;
    private String c_name;
    private int c_duration;
    private int c_since;

    public course() {
    }

    public int getC_id() {
        return c_id;
    }

    public void setC_id(int c_id) {
        this.c_id = c_id;
    }

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    public int getC_duration() {
        return c_duration;
    }

    public void setC_duration(int c_duration) {
        this.c_duration = c_duration;
    }

    public int getC_since() {
        return c_since;
    }

    public void setC_since(int c_since) {
        this.c_since = c_since;
    }

 
    
    
}
